		'@phan-var-force ModelePDFMyObject $module';

		if ($module->write_file($tmpobject, $langs) > 0) {
			header("Location: ".DOL_URL_ROOT."/document.php?modulepart=subventions-".strtolower($tmpobjectkey)."&file=SPECIMEN.pdf");
			return;
